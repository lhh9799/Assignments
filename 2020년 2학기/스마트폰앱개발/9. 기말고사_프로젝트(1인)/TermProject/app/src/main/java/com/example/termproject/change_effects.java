package com.example.termproject;

public class change_effects {
    String Outline;
    String Body;

    public change_effects() {}

    public change_effects(String Outline, String Body) {
        this.Outline = Outline;
        this.Body = Body;
    }

    public String getOutline() {
        return Outline;
    }

    public void setOutline(String Outline) {
        this.Outline = Outline;
    }

    public String getBody() {
        return Body;
    }

    public void setBody(String Body) {
        this.Body = Body;
    }
}
